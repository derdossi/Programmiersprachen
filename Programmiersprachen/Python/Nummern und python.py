import math

pi = 3.14


print(round(pi))  # !Nummer runden
print(math.ceil(pi))  # !Modul math benutzen um immer hoch zu runden
print(math.floor(pi))  # !immer runter runden
print(abs(pi))  # !Absolutwert(absolute value) feststellen
print(pow(pi, 2))  # !Hochrechnen mit einer bestimmten Zahl. Hier 2
print(math.sqrt(pi))  # !Wurzel ziehen

